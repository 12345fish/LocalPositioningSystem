#include "drivers\stm32f4_system.h"
#include "modules\serial.h"

#include "uwbTag.h"
/*====================================================================================================*/
/*====================================================================================================*/
void UART_RecvEven( void )
{
  uint8_t recvData = Serial_RecvByte();
  if(recvData == 0x0D)
    Serial_SendStr("\r\n");
  else
    Serial_SendByte(recvData);
}

void UWBT_Init( void )
{
  UWBT_GPIO_Config();
  UWBT_UART_Config(UART_RecvEven);
}
/*====================================================================================================*/
/*====================================================================================================*/
void UWBT_Loop( void )
{
  while(1) {
    if(KEY_Read()) {
      printf("\f");
    }
  }
}
