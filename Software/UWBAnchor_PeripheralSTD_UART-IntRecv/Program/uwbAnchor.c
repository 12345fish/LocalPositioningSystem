#include "drivers\stm32f3_system.h"
#include "modules\serial.h"

#include "uwbAnchor.h"
/*====================================================================================================*/
/*====================================================================================================*/
void UART_RecvEven( void )
{
  uint8_t recvData = Serial_RecvByte();
  if(recvData == 0x0D)
    Serial_SendStr("\r\n");
  else
    Serial_SendByte(recvData);
}

void UWBA_Init( void )
{
  UWBA_GPIO_Config();
  UWBA_UART_Config(UART_RecvEven);
}
/*====================================================================================================*/
/*====================================================================================================*/
void UWBA_Loop( void )
{
  while(1) {
    if(KEY_Read()) {
      printf("\f");
    }
  }
}
