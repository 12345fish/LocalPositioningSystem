#include "drivers\stm32f4_system.h"
#include "modules\serial.h"
#include "modules\kSerial.h"

#include "uwbAdapter.h"
/*====================================================================================================*/
/*====================================================================================================*/
void UWBA_Init( void )
{
  UWBA_GPIO_Config();
  UWBA_UART_Config(NULL);
}
/*====================================================================================================*/
/*====================================================================================================*/
void UWBA_Loop( void )
{
  float32_t count = 0;
  float32_t sendBuf[4] = {0};

  while(1) {
    sendBuf[0] = 1;
    sendBuf[1] = 2;
    sendBuf[2] = 3;
    sendBuf[3] = count++;
    kSerial_sendData(sendBuf, 4, KS_FLOAT32);
  }
}
