void testFlash( void )
{
#if FLASH_RW_U8
  Flash_EraseSector(FLASH_RW_PAGE);
  Flash_WritePageU8(FLASH_RW_PAGE, WriteDataU8, FLASH_RW_SIZE);
  Flash_ReadPageU8(FLASH_RW_PAGE, ReadDataU8, FLASH_RW_SIZE);
  if(Cmp_ArrU8(WriteDataU8, ReadDataU8, FLASH_RW_SIZE) == SUCCESS)
    LED_R_Reset();
#endif
  delay_ms(500);

#if FLASH_RW_U16
  Flash_EraseSector(FLASH_RW_PAGE);
  Flash_WritePageU16(FLASH_RW_PAGE, WriteDataU16, FLASH_RW_SIZE >> 1);
  Flash_ReadPageU16(FLASH_RW_PAGE, ReadDataU16, FLASH_RW_SIZE >> 1);
  if(Cmp_ArrU16(WriteDataU16, ReadDataU16, FLASH_RW_SIZE >> 1) == SUCCESS)
    LED_G_Reset();
#endif
  delay_ms(500);

#if FLASH_RW_U32
  Flash_EraseSector(FLASH_RW_PAGE);
  Flash_WritePageU32(FLASH_RW_PAGE, WriteDataU32, FLASH_RW_SIZE >> 2);
  Flash_ReadPageU32(FLASH_RW_PAGE, ReadDataU32, FLASH_RW_SIZE >> 2);
  if(Cmp_ArrU32(WriteDataU32, ReadDataU32, FLASH_RW_SIZE >> 2) == SUCCESS)
    LED_B_Reset();
#endif
  delay_ms(500);

#if FLASH_RW_U64
  Flash_EraseSector(FLASH_RW_PAGE);
  Flash_WritePageU64(FLASH_RW_PAGE, WriteDataU64, FLASH_RW_SIZE >> 3);
  Flash_ReadPageU64(FLASH_RW_PAGE, ReadDataU64, FLASH_RW_SIZE >> 3);
  if(Cmp_ArrU64(WriteDataU64, ReadDataU64, FLASH_RW_SIZE >> 3) == SUCCESS) {
    LED_R_Toggle();
    delay_ms(500);
    LED_R_Toggle();
    delay_ms(500);
  }
#endif
  delay_ms(1000);
}
