#include "drivers\stm32f4_system.h"

#include "uwbTag.h"
/*====================================================================================================*/
/*====================================================================================================*/
void UWBT_Init( void )
{
  UWBT_GPIO_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void UWBT_Loop( void )
{
  while(1) {
    LED_R_Toggle();
    delay_ms(100);
    LED_G_Toggle();
    delay_ms(100);
    LED_B_Toggle();
    delay_ms(100);
    while(KEY_Read()) {
      LED_R_Toggle();
      LED_G_Toggle();
      LED_B_Toggle();
      delay_ms(50);
    }
  }
}
